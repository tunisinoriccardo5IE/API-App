def share_track(track_id):
    """Generate a Spotify link for a track."""
    return f"https://open.spotify.com/track/{track_id}"

def share_artist(artist_id):
    """Generate a Spotify link for an artist."""
    return f"https://open.spotify.com/artist/{artist_id}"

def share_playlist(playlist_id):
    """Generate a Spotify link for a playlist."""
    return f"https://open.spotify.com/playlist/{playlist_id}"